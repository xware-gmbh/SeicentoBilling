
package ch.xwr.seicentobilling.dal;

import java.util.List;

import javax.persistence.EntityManager;
import javax.persistence.TypedQuery;
import javax.persistence.criteria.CriteriaBuilder;
import javax.persistence.criteria.CriteriaQuery;
import javax.persistence.criteria.ParameterExpression;
import javax.persistence.criteria.Root;

import com.rapidclipse.framework.server.jpa.dal.JpaDataAccessObject;

import ch.xwr.seicentobilling.entities.Entity;
import ch.xwr.seicentobilling.entities.RowObject;
import ch.xwr.seicentobilling.entities.RowObject_;

/**
 * Home object for domain model class RowObject.
 *
 * @see RowObject
 */
public class RowObjectDAO extends JpaDataAccessObject.Default<RowObject, Long> {
	public RowObjectDAO() {
		super(RowObject.class);
	}

	public RowObject getObjectBase(final String entName, final long id)
	{
		final EntityDAO entDao = new EntityDAO();
		final Entity ent = entDao.findEntity(entName);

		final List<RowObject> li = findObjectBase(ent, id);
		if (li.size() == 0) {
			return null;
		}
		return li.get(0);
	}

	/**
	 * @queryMethod Do not edit, method is generated by editor!
	 */
	public List<RowObject> findObjectBase(final Entity ent, final long id) {
		final EntityManager entityManager = em();

		final CriteriaBuilder criteriaBuilder = entityManager.getCriteriaBuilder();

		final ParameterExpression<Entity> entParameter = criteriaBuilder.parameter(Entity.class, "ent");
		final ParameterExpression<Long> idParameter = criteriaBuilder.parameter(Long.class, "id");

		final CriteriaQuery<RowObject> criteriaQuery = criteriaBuilder.createQuery(RowObject.class);

		final Root<RowObject> root = criteriaQuery.from(RowObject.class);

		criteriaQuery.where(criteriaBuilder.and(criteriaBuilder.equal(root.get(RowObject_.objRowId), idParameter),
				criteriaBuilder.equal(root.get(RowObject_.entity), entParameter)));

		final TypedQuery<RowObject> query = entityManager.createQuery(criteriaQuery);
		query.setParameter(entParameter, ent);
		query.setParameter(idParameter, id);
		return query.getResultList();
	}
}