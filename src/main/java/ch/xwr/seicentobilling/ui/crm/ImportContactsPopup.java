	private void processUploadedFile(final File outFile)
	{
		final CrmExcelHandler   exc = new CrmExcelHandler();
		final List<CustomerDto> lst = exc.readContactsToList(outFile);

		if(!lst.isEmpty())
		{
			this.labelSize.setText("Zeilen: " + lst.size());
		}

		outFile.delete();

	}

