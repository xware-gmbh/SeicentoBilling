
package ch.xwr.seicentobilling.dal;

import java.util.List;

import javax.persistence.EntityManager;
import javax.persistence.TypedQuery;
import javax.persistence.criteria.CriteriaBuilder;
import javax.persistence.criteria.CriteriaQuery;
import javax.persistence.criteria.ParameterExpression;
import javax.persistence.criteria.Root;

import com.xdev.dal.JPADAO;

import ch.xwr.seicentobilling.entities.AppUser;
import ch.xwr.seicentobilling.entities.AppUser_;
import ch.xwr.seicentobilling.entities.CostAccount;

/**
 * Home object for domain model class CostAccount.
 *
 * @see CostAccount
 */
public class AppUserDAO extends JPADAO<AppUser, Long> {
	public AppUserDAO() {
		super(AppUser.class);
	}

	/**
	 * @queryMethod Do not edit, method is generated by editor!
	 */
	public List<AppUser> findByName(final String name) {
		final EntityManager entityManager = em();

		final CriteriaBuilder criteriaBuilder = entityManager.getCriteriaBuilder();

		final ParameterExpression<String> nameParameter = criteriaBuilder.parameter(String.class, "name");

		final CriteriaQuery<AppUser> criteriaQuery = criteriaBuilder.createQuery(AppUser.class);

		final Root<AppUser> root = criteriaQuery.from(AppUser.class);

		criteriaQuery.where(criteriaBuilder.equal(root.get(AppUser_.username), nameParameter));

		final TypedQuery<AppUser> query = entityManager.createQuery(criteriaQuery);
		query.setParameter(nameParameter, name);
		return query.getResultList();
	}
}